package xyza;

//this is example for relational operators
public class Operators2 {
	public static void main (String[] args) {
		int a =10, b=20;
		System.out.println((a>b));
		System.out.println((a>=b));
		System.out.println((a==b));
		
		
		System.out.println((a<b));
		System.out.println((a<=b));
		System.out.println((a!=b));
	}

}
